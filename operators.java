public class operators {
    public static void main(String[] args) {
        int num1=12,num2=10;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
        System.out.println(num1 != num2);
        System.out.println(num1 > num2);
        System.out.println(num1 < num2);
        num1 += 1;
        System.out.println(num1);
        num1 -= 1;
        System.out.println(num1);
        num1 *= 10;
        System.out.println(num1);
        num1 /= 12;
        System.out.println(num1);
        System.out.println(6>2 && 5<10);
        System.out.println(10<2 || 12>10);
        System.out.println(num1++);
        System.out.println(num1);
        System.out.println(++num1);
        System.out.println(--num1);
        System.out.println(num1--);
        System.out.println(num1);
    }
}
